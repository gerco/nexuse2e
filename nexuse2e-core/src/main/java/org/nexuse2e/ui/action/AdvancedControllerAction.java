package org.nexuse2e.ui.action;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.commons.lang.StringUtils;
import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;
import org.apache.struts.action.ActionMessages;
import org.nexuse2e.Engine;
import org.nexuse2e.configuration.EngineConfiguration;

/**
 * used for sending specific commands to dedicated NEXUSe2e instances. Works for standalone instances
 * and clustered environments.
 *
 */
public class AdvancedControllerAction extends NexusE2EAction {

    /* (non-Javadoc)
     * @see org.nexuse2e.ui.action.NexusE2EAction#executeNexusE2EAction(org.apache.struts.action.ActionMapping, org.apache.struts.action.ActionForm, javax.servlet.http.HttpServletRequest, javax.servlet.http.HttpServletResponse, org.apache.struts.action.ActionMessages, org.apache.struts.action.ActionMessages)
     */
    @Override
    public ActionForward executeNexusE2EAction( ActionMapping actionMapping, ActionForm actionForm,
            HttpServletRequest request, HttpServletResponse response, EngineConfiguration engineConfiguration, ActionMessages errors, ActionMessages messages )
            throws Exception {

        ActionForward success = actionMapping.findForward( ACTION_FORWARD_SUCCESS );

        String instanceId = (String)request.getParameter("instanceId");
        String commandId = (String)request.getParameter("commandId");
        if(StringUtils.isNotEmpty(instanceId) && StringUtils.isNotEmpty(commandId) ) {
        	Engine.getInstance().getEngineController().getAdvancedController().executeCommand(instanceId, commandId);
        }
        
        return success;
    }

}
